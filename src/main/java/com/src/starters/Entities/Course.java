package com.src.starters.Entities;

public class Course {
	 String coursename;
	 String  course_teacher_mail ;
	 String course_objective ;
	
	public String getCoursename() {
		return coursename;
	}

	public void setCoursename(String coursename) {
		this.coursename = coursename;
	}

	public String getCourse_teacher_mail() {
		return course_teacher_mail;
	}

	public void setCourse_teacher_mail(String course_teacher_mail) 
	{
		this.course_teacher_mail = course_teacher_mail;
	}

	public String getCourse_objective() 
	{
		return course_objective;
	}

	public void setCourse_objective(String course_objective) {
		this.course_objective = course_objective;
	}
	
	
	

}
